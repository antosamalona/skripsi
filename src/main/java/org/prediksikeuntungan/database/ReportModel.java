package org.prediksikeuntungan.database;

/**
 *
 * @author thread009
 */
public class ReportModel {
    
    String no;
    String pekerjaan;
    String pendapatan;
    String statusMenikah;
    String keuntungan;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getPekerjaan() {
        return pekerjaan;
    }

    public void setPekerjaan(String pekerjaan) {
        this.pekerjaan = pekerjaan;
    }

    public String getPendapatan() {
        return pendapatan;
    }

    public void setPendapatan(String pendapatan) {
        this.pendapatan = pendapatan;
    }

    public String getStatusMenikah() {
        return statusMenikah;
    }

    public void setStatusMenikah(String statusMenikah) {
        this.statusMenikah = statusMenikah;
    }

    public String getKeuntungan() {
        return keuntungan;
    }

    public void setKeuntungan(String keuntungan) {
        this.keuntungan = keuntungan;
    }
    
    
    
}
